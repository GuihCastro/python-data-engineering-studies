list = ["p", "y", "t", "h", "o", "n"]

print(list[2:]) # elemento 2 em diante
print(list[:2]) # até o elemento 2
print(list[1:3]) # elementos de 1 a 3
print(list[0:4:2]) # elementos de 0 a 4, de 2 em 2 índices
print(list[::]) # todos os elementos
print(list[::-1]) # todos os elementos, de trás para frente