name = "Guilherme"
age = 32
profession = "Programador"
language = "Python"
PI = 3.14159

# .format
print("Olá! Meu nome é {}, tenho {} anos e sou um {} estudando {}.".format(name, age, profession, language))
# fstring
print(f"Olá! Me chamo {name}, tenho {age} anos de idade e sou um {profession}. Atualmente estou estudando {language}")
# formatando casas decimais
print(f"Valor de PI: {PI:.2f}")