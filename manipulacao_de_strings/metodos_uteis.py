course = "python"

# title
print(course.title())
# upper
print(course.upper())
# lower
print(course.lower())

course = "   Python   "

# strip
print(course.strip())
# lstrip
print(course.lstrip())
# rstrip
print(course.rstrip())

course = "Python"

# center
print(course.center(10, "#"))
# join
print(".".join(course))